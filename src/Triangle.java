class Triangle {
    private int sideA;
    private int sideB;
    private int sideC;
    private int sum;
    private int squareRoot;

    public int getSum() {
        return sum;
    }

    void setSum(int sum) {
        this.sum = sum;
    }

    public int getSquareRoot() {
        return squareRoot;
    }

    void setSquareRoot(int squareRoot) {
        this.squareRoot = squareRoot;
    }

    Integer getSideA() {
        return sideA;
    }

    void setSideA(Integer sideA) {
        this.sideA = sideA;
    }

    Integer getSideB() {
        return sideB;
    }

    void setSideB(Integer sideB) {
        this.sideB = sideB;
    }

    Integer getSideC() {
        return sideC;
    }

    void setSideC(Integer sideC) {
        this.sideC = sideC;
    }
}
